let countrySelected = $('#id_default_country').val();
// Fix to the Profile countryfield dropwdown, when a country is selected the color of the font is changed to black
if(!countrySelected) {
    $('#id_default_country').css('color', '#aab7c4');
}
$('#id_default_country').change(function() {
    countrySelected = $(this).val();
    if(!countrySelected) {
        $(this).css('color', '#aab7c4');
    } else {
        $(this).css('color', '#000');
    }
});